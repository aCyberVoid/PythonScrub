# BASIC MATH WITHOUT MATH MODULE
# add - integer
print(50 + 50)
# subtract - integer
print(50 - 50)
# multiply - integer
print(50 * 50)
# divide - float by default
print(50 / 50)
# PEMDAS
print(50 + 50 - 50 * 50 / 50)
# Exponents
print(50 ** 2)
# modulo - takes what's left over
print(50 % 6)
# division with remainder - float
print(50 / 6)
# division - no remainder
print(50 // 6)